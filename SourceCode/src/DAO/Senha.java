package DAO;

public class Senha {
	private String senhaGeral;
	private String senhaMaster;
	public String getSenhaGeral() {
		return senhaGeral;
	}
	public void setSenhaGeral(String senhaGeral) {
		this.senhaGeral = senhaGeral;
	}
	public String getSenhaMaster() {
		return senhaMaster;
	}
	public void setSenhaMaster(String senhaMaster) {
		this.senhaMaster = senhaMaster;
	}
}
